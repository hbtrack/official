def validate_summary_schema(summary_file: Path) -> bool:
    """Validate that summary.json follows required strict-minimal schema"""
    try:
        with open(summary_file, 'r', encoding='utf-8') as f:
            data = json.load(f)
        
        required_keys = ["task_id", "status", "timestamp", "commit_hash", "brief_description"]
        
        # Verify all required keys are present
        for key in required_keys:
            if key not in data:
                print(f"ERROR: Missing required key '{key}' in summary.json", file=sys.stderr)
                return False
        
        # Verify NO extra keys (Strict-Minimal)
        if len(data) > len(required_keys):
            extra_keys = set(data.keys()) - set(required_keys)
            print(f"ERROR: Extra keys found in strict-minimal summary.json: {extra_keys}", file=sys.stderr)
            return False

        # Validate status enum
        if data["status"] not in ["PASS", "FAIL", "DRIFT"]:
            print(f"ERROR: Invalid status '{data['status']}'. Must be PASS|FAIL|DRIFT", file=sys.stderr)
            return False
        
        # Validate brief_description length
        if len(data["brief_description"]) > 120:
            print(f"ERROR: brief_description too long ({len(data['brief_description'])} chars, max 120)", file=sys.stderr)
            return False
        
        return True
        
    except json.JSONDecodeError as e:
        print(f"ERROR: Invalid JSON in summary.json: {e}", file=sys.stderr)
        return False
    except Exception as e:
        print(f"ERROR: Validation failed: {e}", file=sys.stderr)
        return False


def main():
    parser = argparse.ArgumentParser(description='Archive execution task artifacts')
    parser.add_argument('--task-id', required=True, help='Task ID (e.g., EXEC-AUTH-001)')
    parser.add_argument('--status', required=True, choices=['PASS', 'FAIL', 'DRIFT'],
                       help='Task execution status')
    parser.add_argument('--description', required=True, help='Brief description (max 120 chars)')
    parser.add_argument('--timestamp', help='ISO8601 timestamp (default: now UTC)')
    parser.add_argument('--commit', help='Git commit hash (default: current HEAD)')
    parser.add_argument('--log-file', help='Path to execution log file to copy')
    
    args = parser.parse_args()
    
    # Validate inputs
    if len(args.description) > 120:
        print(f"ERROR: Description too long ({len(args.description)} chars, max 120)", file=sys.stderr)
        return 2
    
    # Set defaults
    timestamp = args.timestamp or datetime.now(timezone.utc).isoformat()
    commit = args.commit or get_git_commit_hash()
    
    try:
        # Get repo root
        repo_root = Path.cwd()
        
        # Create artifacts directory
        artifacts_dir = create_artifacts_directory(args.task_id, repo_root)
        print(f"Created artifacts directory: {artifacts_dir}")
        
        # Create summary.json
        summary_file = create_summary_json(
            args.task_id, args.status, args.description, timestamp, commit, artifacts_dir)
        print(f"Created summary.json: {summary_file}")
        
        # Create placeholder files
        log_file, evidence_file = create_placeholder_files(args.task_id, artifacts_dir, timestamp, args.log_file)
        
        print(f"SUCCESS: Task {args.task_id} archived successfully")
        return 0
        
    except Exception as e:
        print(f"ERROR: Runtime error: {e}", file=sys.stderr)
        return 3


if __name__ == "__main__":
    sys.exit(main())