#!/usr/bin/env python3
"""
Archive Task Script - Creates execution_tasks artifacts structure
Part of ARCH-DOCS-ARTIFACTS-002 implementation

Purpose: Create artifacts/<TASK_ID>/ and write summary.json + placeholders
"""

import argparse
import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path
import subprocess


def get_git_commit_hash():
    """Get current git commit hash (short)"""
    try:
        result = subprocess.run(['git', 'rev-parse', '--short', 'HEAD'], 
                              capture_output=True, text=True, check=True)
        return result.stdout.strip()
    except (subprocess.CalledProcessError, FileNotFoundError):
        return "unknown"


def create_artifacts_directory(task_id: str, base_path: Path) -> Path:
    """Create artifacts directory for task"""
    artifacts_dir = base_path / "docs" / "execution_tasks" / "artifacts" / task_id
    artifacts_dir.mkdir(parents=True, exist_ok=True)
    return artifacts_dir


